import math

# ceil() - Returns the smallest integer greater than or equal to x.
print(math.ceil(4.2))  # Output: 5

# floor() - Returns the largest integer less than or equal to x.
print(math.floor(4.8))  # Output: 4

# sqrt() - Returns the square root of x.
print(math.sqrt(16))  # Output: 4.0

# pow() - Returns x raised to the power of y.
print(math.pow(2, 3))  # Output: 8.0

# fabs() - Returns the absolute value of x.
print(math.fabs(-5.5))  # Output: 5.5

# factorial() - Returns the factorial of x.
print(math.factorial(5))  # Output: 120

# gcd() - Returns the greatest common divisor of x and y.
print(math.gcd(48, 18))  # Output: 6

# exp() - Returns e raised to the power of x.
print(math.exp(2))  # Output: 7.3890560989306495

# log() - Returns the natural logarithm of x (to base e).
print(math.log(10))  # Output: 2.302585092994046

# log10() - Returns the base-10 logarithm of x.
print(math.log10(100))  # Output: 2.0

# sin() - Returns the sine of x (x is in radians).
print(math.sin(math.pi / 2))  # Output: 1.0

# cos() - Returns the cosine of x (x is in radians).
print(math.cos(math.pi))  # Output: -1.0

# tan() - Returns the tangent of x (x is in radians).
print(math.tan(math.pi / 4))  # Output: 1.0

# degrees() - Converts angle x from radians to degrees.
print(math.degrees(math.pi))  # Output: 180.0

# radians() - Converts angle x from degrees to radians.
print(math.radians(180))  # Output: 3.141592653589793

# pi - The mathematical constant Ï€ = 3.141592653589793.
print(math.pi)  # Output: 3.141592653589793

# e - The mathematical constant e = 2.718281828459045.
print(math.e)  # Output: 2.718281828459045